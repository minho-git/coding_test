N = int(input())
arr = [list(map(int,input().split())) for _ in range(N)]
total = 0
s = e = N // 2

for y in range(N):
    total += sum(arr[y][s:e + 1])
    print(s, e)

    if y < N // 2:
        s -= 1
        e += 1
    else:
        s += 1
        e -= 1






